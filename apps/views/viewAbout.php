<h2>Sobre LumenEdu</h2>

<p>
    LumenEdu es una plataforma educativa que conecta <strong>profesores</strong> con <strong>alumnos</strong>
    de distintas materias. Los creadores de contenido pueden publicar clases, videos, artículos
    e imágenes para sus suscriptores.
</p>

<h3>¿Cómo funciona?</h3>
<ul>
    <li><strong>Profesores (Creadores):</strong> suben contenido y ofrecen acceso a sus clases mediante suscripción.</li>
    <li><strong>Alumnos (Suscriptores):</strong> se suscriben a sus profesores favoritos y acceden al material.</li>
    <li><strong>Mensajería:</strong> los usuarios pueden comunicarse directamente dentro de la plataforma.</li>
</ul>
